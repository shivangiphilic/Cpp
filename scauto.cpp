//the datatypes of auto variables are deduced by the compiler
#include <iostream>
using namespace std;
int main()
{
    auto a = 5;
    auto b = "V";
    auto c = "University";
    auto d = 9.8;
    cout << a << "\n";
    cout << b << "\n";
    cout << c << "\n";
    cout << d << "\n";
}
