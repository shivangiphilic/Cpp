#include <iostream>
using namespace std;

//To print automatically datatyped var, like Py, use auto datatype
int main()
{
    auto a = 7;
    auto b = "Hello";
    auto c = "V";
    auto d = 9.80;
    cout << a << "\n";
    cout << b << "\n";
    cout << c << "\n";
    cout << d << "\n";
}
